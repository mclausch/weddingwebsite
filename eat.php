			<h3 class="heading-<?php echo $gPageColor; ?>"><strong>Markets and Food Trucks</strong></h3>
			<p>San Francisco has a ton of food markets to explore and the food truck culture is also very strong. Be sure to check some out for cheap, fresh, and innovative food.</p>
